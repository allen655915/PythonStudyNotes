# 割圆法算圆周率,设圆的半径r=1,则初始六边形边长a=1
# 高h=sqrt(r^2-(a/2)^2)
# 十二边形的边长a1=sqrt((a/2)^2+(r-h)^2)
import math
n = 6 # 边数
r = 2 # 圆的半径
a = 2 # 初始六边形边长
# 循环一次，割圆一次
for i in range(1,30):
    h = math.sqrt(r**2 - (a/2)**2) # 勾股定理计算圆心到边的垂线长度
    a = math.sqrt((a/2)**2 + (r-h)**2) # 勾股定理计算新多边形，边的长度
    n = n*2 # 每割一次边数增加一倍
    c = a*n # 计算多边形的周长
    pi = c/(2*r) # 圆周率等于周长除以直径
    print(pi)